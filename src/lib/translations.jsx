// ─── Language Context ─────────────────────────────────────────────────────────
const LanguageContext = createContext();

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState("es");
  const [translationsData, setTranslationsData] = useState(translations);
  const [loading, setLoading] = useState(false);

  useEffect(() => {
    const loadLang = async () => {
      try {
        const { data, error } = await supabase
          .from('languages')
          .select('key, es, en, zh');

        if (error) {
          console.log("Using local translations (table not available)");
          return;
        }
        
        if (data && data.length > 0) {
          const loaded = { es: {}, en: {}, zh: {} };
          data.forEach(row => {
            if (row.key) {
              ['es', 'en', 'zh'].forEach(l => {
                if (row[l]) loaded[l][row.key] = row[l];
              });
            }
          });
          
          setTranslationsData(prev => ({
            es: { ...prev.es, ...loaded.es },
            en: { ...prev.en, ...loaded.en },
            zh: { ...prev.zh, ...loaded.zh }
          }));
        }
      } catch (err) {
        console.log("Using local translations");
      }
    };
    loadLang();
  }, []);

  const tBase = (key) => {
    return translationsData[lang]?.[key] || translations[lang]?.[key] || key;
  };

  const t = new Proxy(tBase, {
    get: (target, prop) => {
      if (typeof prop === 'string' && prop !== 'then' && prop !== '$$typeof') {
        return translationsData[lang]?.[prop] || translations[lang]?.[prop] || prop;
      }
      return target[prop];
    }
  });

  return (
    <LanguageContext.Provider value={{ lang, setLang, t, loading }}>
      {children}
    </LanguageContext.Provider>
  );
}

export const useLanguage = () => useContext(LanguageContext);
